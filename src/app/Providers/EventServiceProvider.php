        'App\Events\AccessDetectionEvent' => [
            // アクセスリスナー
            'App\Listeners\AccessIpRecordListener',
        ]
    ];

    /**
     * Register any events for your application.
     *
     * @return void
     */
    public function boot()
    {
        //
    }
}
